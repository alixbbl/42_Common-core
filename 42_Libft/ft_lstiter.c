#include"libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
/*
On avance de structure en structure grace a "lst = lst->next". A chaque
structure, le contenu de type *content, est passe en parametre de la fonction
f. Cf strmapi.*/
